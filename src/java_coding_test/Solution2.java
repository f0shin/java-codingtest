package java_coding_test;

import java.util.Scanner;

public class Solution2 {
	public static void main(String[] args) {
		// ★문자열 a, b를 입력 받아서 출력
		Scanner sc = new Scanner(System.in);
		String a = sc.next();
		String b = sc.next();

		System.out.println("a = " + a);
		System.out.println("b = " + b);
		
		sc.close();
	}
}
